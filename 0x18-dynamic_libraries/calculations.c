#include "calculations.h"
/**
  * add - sums two numbers
  * @a: first number
  * @b: second number
  *
  * Return: returns an integer
  */
int add(int a, int b)
{
	return(a + b);
}


/**
  * sub - subtract two numbers
  * @a: first number
  * @b: second number
  *
  * Return: returns an integer
  */
int sub(int a, int b)
{
	return(a - b);
}

/**
  * sub - subtract two numbers
  * @a: first number
  * @b: second number
  *
  * Return: returns an integer
  */
int mul(int a, int b)
{
	return(a * b);
}

/**
  * div - divides two numbers
  * @a: first number
  * @b: second number
  *
  * Return: returns an integer
  */
int div(int a, int b)
{
	return(a / b);
}
/**
  * mod - finds remainder of two numbers
  * @a: first number
  * @b: second number
  *
  * Return: returns an integer
  */
int mod(int a, int b)
{
	return(a % b);
}
